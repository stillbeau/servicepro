import PDFDocument from 'pdfkit';

function generatePdf(data) {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ size: 'LETTER', margin: 50 });
    const chunks = [];
    doc.on('data', (chunk) => chunks.push(chunk));
    doc.on('end', () => resolve(Buffer.concat(chunks)));
    doc.on('error', reject);

    const green = '#5b7b2f';
    const gray = '#6b6b6b';
    const red = '#c0392b';

    // --- Header bar ---
    doc.rect(0, 0, doc.page.width, 80).fill(green);
    doc.fillColor('#ffffff').fontSize(22).font('Helvetica-Bold')
      .text('Countertop Service Request', 50, 28);
    doc.moveDown(2);
    doc.y = 100;

    // --- Date ---
    doc.fillColor(gray).fontSize(9).font('Helvetica')
      .text(`Submitted: ${new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })}`, 50);
    doc.moveDown(1);

    // --- Section helper ---
    function sectionHeader(title) {
      doc.moveDown(0.5);
      doc.fillColor(green).fontSize(13).font('Helvetica-Bold').text(title);
      doc.moveTo(50, doc.y + 2).lineTo(doc.page.width - 50, doc.y + 2).strokeColor('#d6d9d0').lineWidth(1).stroke();
      doc.moveDown(0.5);
    }

    function fieldRow(label, value) {
      doc.fillColor(gray).fontSize(10).font('Helvetica').text(label, 50, doc.y, { continued: true, width: 130 });
      doc.fillColor('#2d2d2d').font('Helvetica').text('  ' + (value || 'Not provided'), { lineGap: 4 });
    }

    // --- Contact Information ---
    sectionHeader('Contact Information');
    fieldRow('Name', `${data.firstName} ${data.lastName}`);
    fieldRow('Email', data.email);
    fieldRow('Phone', data.phone);
    fieldRow('Address', data.address);

    // --- Service Details ---
    sectionHeader('Service Details');
    fieldRow('Request Type', data.requestType);
    fieldRow('Material', data.material);
    fieldRow('Install Date', data.installDate || 'Not provided');

    // Urgency with color
    doc.fillColor(gray).fontSize(10).font('Helvetica').text('Urgency', 50, doc.y, { continued: true, width: 130 });
    const urgencyColor = data.urgency === 'Normal' ? green : red;
    const urgencyText = data.urgency === 'Normal' ? 'Normal' : 'Urgent - Affecting daily use';
    doc.fillColor(urgencyColor).font('Helvetica-Bold').text('  ' + urgencyText, { lineGap: 4 });

    // --- Description ---
    sectionHeader('Description');
    doc.fillColor('#2d2d2d').fontSize(10).font('Helvetica').text(data.description, 50, doc.y, {
      width: doc.page.width - 100,
      lineGap: 3,
    });

    // --- Photos ---
    if (data.photos && data.photos.length > 0) {
      sectionHeader(`Photos (${data.photos.length})`);

      for (const photo of data.photos) {
        try {
          const imgBuffer = Buffer.from(photo.content, 'base64');

          // Start a new page if less than 250pt remaining
          if (doc.y > doc.page.height - 250) {
            doc.addPage();
          }

          doc.image(imgBuffer, 50, doc.y, {
            fit: [doc.page.width - 100, 350],
          });
          doc.moveDown(1);
          // Move y past the image
          doc.y += 10;

          doc.fillColor(gray).fontSize(8).font('Helvetica').text(photo.name, 50);
          doc.moveDown(1);
        } catch {
          doc.fillColor(red).fontSize(9).text(`Could not embed image: ${photo.name}`);
          doc.moveDown(0.5);
        }
      }
    }

    // --- Footer ---
    doc.moveDown(1);
    doc.fillColor(gray).fontSize(8).font('Helvetica')
      .text('Generated by FloForm Service Request System', 50, doc.page.height - 40, { align: 'center', width: doc.page.width - 100 });

    doc.end();
  });
}

export default async (req) => {
  // Only allow POST
  if (req.method !== 'POST') {
    return new Response(JSON.stringify({ error: 'Method not allowed' }), {
      status: 405,
      headers: { 'Content-Type': 'application/json' },
    });
  }

  const BREVO_API_KEY = process.env.BREVO_API_KEY;
  if (!BREVO_API_KEY) {
    return new Response(JSON.stringify({ error: 'Server configuration error.' }), {
      status: 500,
      headers: { 'Content-Type': 'application/json' },
    });
  }

  let data;
  try {
    data = await req.json();
  } catch {
    return new Response(JSON.stringify({ error: 'Invalid request body.' }), {
      status: 400,
      headers: { 'Content-Type': 'application/json' },
    });
  }

  // Validate required fields
  const required = ['firstName', 'lastName', 'email', 'phone', 'address', 'requestType', 'material', 'description'];
  for (const field of required) {
    if (!data[field] || !data[field].trim()) {
      return new Response(JSON.stringify({ error: `Missing required field: ${field}` }), {
        status: 400,
        headers: { 'Content-Type': 'application/json' },
      });
    }
  }

  if (!data.photos || data.photos.length === 0) {
    return new Response(JSON.stringify({ error: 'At least one photo is required.' }), {
      status: 400,
      headers: { 'Content-Type': 'application/json' },
    });
  }

  // Generate PDF report
  let pdfBase64;
  try {
    const pdfBuffer = await generatePdf(data);
    pdfBase64 = pdfBuffer.toString('base64');
  } catch (err) {
    console.error('PDF generation error:', err);
    // Continue without PDF if generation fails
    pdfBase64 = null;
  }

  // Build HTML email body
  const urgencyBadge = data.urgency === 'Normal'
    ? '<span style="color:#5b7b2f;font-weight:600;">Normal</span>'
    : '<span style="color:#c0392b;font-weight:600;">Urgent - Affecting daily use</span>';

  const htmlContent = `
    <div style="font-family:-apple-system,BlinkMacSystemFont,'Segoe UI',Roboto,Arial,sans-serif;max-width:600px;margin:0 auto;color:#2d2d2d;">
      <div style="background:#5b7b2f;padding:20px 24px;border-radius:8px 8px 0 0;">
        <h1 style="margin:0;color:#fff;font-size:20px;">Countertop Service Request</h1>
      </div>
      <div style="border:1px solid #d6d9d0;border-top:none;padding:24px;border-radius:0 0 8px 8px;">
        <h2 style="color:#5b7b2f;font-size:16px;margin:0 0 16px;border-bottom:2px solid #f0f4ea;padding-bottom:8px;">Contact Information</h2>
        <table style="width:100%;font-size:14px;border-collapse:collapse;">
          <tr><td style="padding:4px 12px 4px 0;color:#6b6b6b;width:140px;">Name</td><td style="padding:4px 0;">${data.firstName} ${data.lastName}</td></tr>
          <tr><td style="padding:4px 12px 4px 0;color:#6b6b6b;">Email</td><td style="padding:4px 0;"><a href="mailto:${data.email}" style="color:#5b7b2f;">${data.email}</a></td></tr>
          <tr><td style="padding:4px 12px 4px 0;color:#6b6b6b;">Phone</td><td style="padding:4px 0;"><a href="tel:${data.phone}" style="color:#5b7b2f;">${data.phone}</a></td></tr>
          <tr><td style="padding:4px 12px 4px 0;color:#6b6b6b;">Service Address</td><td style="padding:4px 0;">${data.address}</td></tr>
        </table>

        <h2 style="color:#5b7b2f;font-size:16px;margin:24px 0 16px;border-bottom:2px solid #f0f4ea;padding-bottom:8px;">Service Details</h2>
        <table style="width:100%;font-size:14px;border-collapse:collapse;">
          <tr><td style="padding:4px 12px 4px 0;color:#6b6b6b;width:140px;">Request Type</td><td style="padding:4px 0;font-weight:600;">${data.requestType}</td></tr>
          <tr><td style="padding:4px 12px 4px 0;color:#6b6b6b;">Material</td><td style="padding:4px 0;">${data.material}</td></tr>
          <tr><td style="padding:4px 12px 4px 0;color:#6b6b6b;">Install Date</td><td style="padding:4px 0;">${data.installDate || 'Not provided'}</td></tr>
          <tr><td style="padding:4px 12px 4px 0;color:#6b6b6b;">Urgency</td><td style="padding:4px 0;">${urgencyBadge}</td></tr>
        </table>

        <h2 style="color:#5b7b2f;font-size:16px;margin:24px 0 16px;border-bottom:2px solid #f0f4ea;padding-bottom:8px;">Description</h2>
        <p style="font-size:14px;line-height:1.6;white-space:pre-wrap;background:#f5f6f3;padding:12px 16px;border-radius:6px;">${data.description}</p>

        <p style="font-size:13px;color:#6b6b6b;margin-top:24px;border-top:1px solid #d6d9d0;padding-top:12px;">${data.photos.length} photo(s) attached to this email.${pdfBase64 ? ' A PDF report is also attached.' : ''}</p>
      </div>
    </div>
  `;

  // Build attachments: photos + PDF report
  const attachments = data.photos.map((photo) => ({
    content: photo.content,
    name: photo.name,
  }));

  if (pdfBase64) {
    attachments.push({
      content: pdfBase64,
      name: `Service-Request_${data.lastName}_${data.firstName}.pdf`,
    });
  }

  // Build Brevo API payload
  const emailPayload = {
    sender: {
      name: 'FloForm Service Request',
      email: 'sam@sccountertops.ca',
    },
    to: [
      { email: 'ccederholm@floform.com', name: 'Craig Cederholm' },
      { email: 'rmclean@floform.com', name: 'Renee McLean' },
    ],
    cc: [
      { email: 'sbeaumont@floform.com', name: 'Sam Beaumont' },
    ],
    replyTo: {
      email: data.email,
      name: `${data.firstName} ${data.lastName}`,
    },
    subject: `Service Request: ${data.requestType} â€“ ${data.firstName} ${data.lastName}`,
    htmlContent: htmlContent,
    attachment: attachments,
  };

  try {
    const brevoRes = await fetch('https://api.brevo.com/v3/smtp/email', {
      method: 'POST',
      headers: {
        'accept': 'application/json',
        'api-key': BREVO_API_KEY,
        'content-type': 'application/json',
      },
      body: JSON.stringify(emailPayload),
    });

    if (!brevoRes.ok) {
      const errBody = await brevoRes.text();
      console.error('Brevo API error:', brevoRes.status, errBody);
      return new Response(JSON.stringify({ error: 'Failed to send email. Please try again later.' }), {
        status: 502,
        headers: { 'Content-Type': 'application/json' },
      });
    }

    return new Response(JSON.stringify({ success: true }), {
      status: 200,
      headers: { 'Content-Type': 'application/json' },
    });

  } catch (err) {
    console.error('Network error sending email:', err);
    return new Response(JSON.stringify({ error: 'Failed to send email. Please try again later.' }), {
      status: 502,
      headers: { 'Content-Type': 'application/json' },
    });
  }
};
